                       <p className="text-[11px] text-blue-200/70 font-bold uppercase tracking-widest">
                         Iniciado em {activeBatch.createdAt ? (activeBatch.createdAt.toDate ? activeBatch.createdAt.toDate().toLocaleDateString('pt-BR') : (isNaN(new Date(activeBatch.createdAt).getTime()) ? '---' : new Date(activeBatch.createdAt).toLocaleDateString('pt-BR'))) : '---'}
                       </p>
