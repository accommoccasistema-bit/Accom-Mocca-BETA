  // Loud and premium digital warning chime synthesizer using Web Audio API
  const playWheatAlertSound = () => {
    try {
      const AudioCtx = window.AudioContext || (window as any).webkitAudioContext;
      if (!AudioCtx) return;
      const ctx = new AudioCtx();
      const now = ctx.currentTime;

      const playTone = (time: number, freq: number, duration: number, volume: number) => {
        const osc = ctx.createOscillator();
        const sub = ctx.createOscillator();
        const gain = ctx.createGain();

        osc.type = 'sine';
        osc.frequency.setValueAtTime(freq, time);
        // Subtle descend sweep to resemble heavy warning/arrival alert
        osc.frequency.exponentialRampToValueAtTime(freq * 0.9, time + duration);

        sub.type = 'triangle';
        sub.frequency.setValueAtTime(freq / 2, time);
        sub.frequency.exponentialRampToValueAtTime(freq * 0.45, time + duration);

        gain.gain.setValueAtTime(volume, time);
        gain.gain.exponentialRampToValueAtTime(0.001, time + duration);

        osc.connect(gain);
        sub.connect(gain);
        gain.connect(ctx.destination);

        osc.start(time);
        sub.start(time);
        osc.stop(time + duration);
        sub.stop(time + duration);
      };

      // Sound signature: 2 loud high-alert metallic beeps + heavy drop/alarm sound
      playTone(now, 1600, 0.15, 0.82);
      playTone(now + 0.18, 1600, 0.15, 0.82);
      playTone(now + 0.36, 1200, 0.35, 0.7);
      playTone(now + 0.52, 750, 0.55, 0.6);
    } catch (e) {
      console.warn("Failed to generate synthetic alert audio:", e);
    }
  };

  // Test notification function for checking mobile compatibility
